#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		in;
	char	out;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb *= (-1);
		write(1, "-", 1);
	}
	in = nb / 10;
	out = nb % 10 + '0';
	if (in != 0)
	{
		ft_putnbr(in);
		write(1, &out, 1);
	}
	else
	{
		in += '0';
		write(1, &out, 1);
	}
}
